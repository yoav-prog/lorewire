"""Export generated story bodies from the SQLite store to the Next.js app, so
the front end can render real pipeline output instead of placeholder text.

Writes a TS module: BODIES = { storyId: articleBody }. Run after a pipeline
run:  python -m pipeline.export_app
"""
from __future__ import annotations

import json
import sqlite3
from pathlib import Path

from pipeline.config import DB_PATH

OUT = Path(__file__).resolve().parent.parent / "lorewire-app" / "src" / "data" / "bodies.ts"


def main() -> None:
    conn = sqlite3.connect(DB_PATH)
    conn.row_factory = sqlite3.Row
    rows = conn.execute("SELECT id, body FROM stories WHERE body IS NOT NULL AND body != ''").fetchall()
    data = {r["id"]: r["body"] for r in rows if not str(r["body"]).startswith("[DRY RUN")}
    OUT.parent.mkdir(parents=True, exist_ok=True)
    body = json.dumps(data, ensure_ascii=True, indent=2)
    ts = "// Generated by `python -m pipeline.export_app` - do not edit by hand.\n"
    ts += "export const BODIES: Record<string, string> = " + body + ";\n"
    OUT.write_text(ts, encoding="utf-8")
    print(f"wrote {len(data)} article bod(ies) to {OUT}")
    for k in data:
        print(f"  - {k} ({len(data[k])} chars)")


if __name__ == "__main__":
    main()
